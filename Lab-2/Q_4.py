# Compute a)5 to the power of 8 b)square root of 400 c)exponent of 5 d)Logarithm of 625 base 5

import math

# a) 5 to the power of 8
power_of_5 = 5 ** 8
print(f"5 to the power of 8 is: {power_of_5}")

# b) Square root of 400
square_root_400 = math.sqrt(400)
print(f"Square root of 400 is: {square_root_400}")

# c) Exponent of 5
exponent_of_5 = math.exp(5)
print(f"Exponent of 5 (e^5) is: {exponent_of_5}")

# d) Logarithm of 625 base 5
log_base_5_of_625 = math.log(625, 5)
print(f"Logarithm of 625 base 5 is: {log_base_5_of_625}")
